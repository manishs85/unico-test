package com.unico.common;

public class Gcd {

	private int id;
	private int gcd;

	public Gcd() {
	}

	public Gcd(int gcd) {
		this.gcd = gcd;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getGcd() {
		return gcd;
	}

	public void setGcd(int gcd) {
		this.gcd = gcd;
	}

}
